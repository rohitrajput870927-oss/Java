package BasicOfJava;

public class ArithmeticOperation5 {
    static void main() {
        //For integer value
        int x=87, y=10;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*x);
        System.out.println(x/y);
        //For decimal value
        System.out.println("For Decimal Value");
        double e=28, c=5;
        System.out.println(e+c);
        System.out.println(e*c);
        System.out.println(e-c);
        System.out.println(e/c);




    }
}
